class Piece
  attr_accessor :colour
  def initialize(color)
    @color = color
  end

  def white?
    @color == :white
  end

  def black?
    !white?
  end

  def valid_edges?(arr_diff)
    arr = edges
    arr.each do |element|
      if element.length == 2 && element == arr_diff
        return true
      elsif element.include? arr_diff
        return true
      end
    end
    false
  end

